package session3.homework;

public class UnaryOperators1 {

    public static void main(String[] args) {
        int counter = 0;

        while (counter <= 5) {
            System.out.println("Counter: " + counter);
            counter++;
        }

        while (counter >= 0) {
            System.out.println("Counter: " + counter);
            counter--;
        }
    }
}
