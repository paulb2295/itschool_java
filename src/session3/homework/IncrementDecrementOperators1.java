package session3.homework;

public class IncrementDecrementOperators1 {

    public static void main(String[] args) {
        int counter;

        for (counter = 1; counter < 11; counter++) {
            System.out.println(counter);
        }
        for (counter = 9; counter > 0; counter--) {
            System.out.println(counter);
        }

    }
}
